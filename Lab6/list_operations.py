def sort_list(lst):
    """Сортує список у зростаючому порядку."""
    return sorted(lst)

def search_element(lst, value):
    """Шукає елемент у списку та повертає його індекс або -1, якщо не знайдено."""
    for index, item in enumerate(lst):
        if item == value:
            return index  # Повертаємо індекс першого знайденого елемента
    return -1  # Якщо значення немає в списку


def search_subsequence(lst, subseq):
    """Шукає послідовність елементів у списку та повертає перший індекс або -1."""
    sub_len = len(subseq)
    for i in range(len(lst) - sub_len + 1):
        if lst[i:i + sub_len] == subseq:
            return i
    return -1

def find_min_five(lst):
    """Повертає список з 5 найменших унікальних елементів."""
    return sorted(set(lst))[:5]

def find_max_five(lst):
    """Повертає список з 5 найбільших унікальних елементів."""
    return sorted(set(lst), reverse=True)[:5]

def calculate_average(lst):
    """Обчислює середнє арифметичне списку чисел."""
    return sum(lst) / len(lst) if lst else 0

def remove_duplicates(lst):
    """Повертає список без дублікатів, зберігаючи порядок."""
    seen = set()
    return [x for x in lst if not (x in seen or seen.add(x))]